/**
 * substring method extracts a substring/set of characters from the string and displays it on the console
 */
public class substringDemo {
    public static void main(String[] args) {
        String str1= "java is fun";

        //extract substring from index 0 to 3
        System.out.println(str1.substring(0,4)); //outcome should be java with the space after a marking index 4
    }
}
