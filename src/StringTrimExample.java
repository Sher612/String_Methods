/**
 * trim() method removes leading and trailing spaces.
 */
public class StringTrimExample {
  public static void main(String[] args) {
      String s1 ="   hello    ";
      System.out.println(s1+"how are you"); //the console will display the statement without trim
      System.out.println(s1.trim()+" how are you");//trimmed version
  }
}
